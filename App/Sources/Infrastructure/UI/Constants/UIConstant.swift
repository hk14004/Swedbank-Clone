import Foundation

enum UIConstant {}

extension UIConstant {
    enum NavigationBar {
        static let barButtonSize: CGFloat = 36
        static let barButtonIconSize: CGFloat = 28
    }
}

extension UIConstant {
    enum Button {
        static let highlightAlpha: CGFloat = 0.5
        static let defaultAlpha: CGFloat = 1
        static let cornerRadius: CGFloat = 16
    }
}
